export function needsSimplifiedChineseTranslation(text: string | null | undefined): boolean {
  const value = text?.trim()
  if (!value) return false
  if (TRADITIONAL_HINT_RE.test(value)) return true
  if (LATIN_RE.test(value) || KANA_RE.test(value) || HANGUL_RE.test(value)) return true

  const hanMatches = value.match(HAN_RE) || []
  if (hanMatches.length === 0) return true

  return false
}

export async function translateNotificationBodyText(
  text: string,
  userId?: number | null,
): Promise<string | null> {
  if (!needsSimplifiedChineseTranslation(text)) return null
  const result = await translateText(text, 'zh', userId)
  const translated = result.fullTextTranslated.trim()
  return translated || null
}
