export type Locale = 'ja' | 'en' | 'zh'

export function normalizeLocale(value: string | null | undefined): Locale | null {
  return value === 'ja' || value === 'en' || value === 'zh' ? value : null
}

export function detectBrowserLocale(language: string | null | undefined): Locale {
  if (language?.startsWith('ja')) return 'ja'
  if (language?.startsWith('zh')) return 'zh'
  return 'en'
}

export function resolvePreferredLocale(args: {
  urlLocale?: string | null
  storedLocale?: string | null
  profileLocale?: string | null
  navigatorLanguage?: string | null
}): Locale {
  return normalizeLocale(args.urlLocale)
    ?? normalizeLocale(args.storedLocale)
    ?? normalizeLocale(args.profileLocale)
    ?? detectBrowserLocale(args.navigatorLanguage)
}

